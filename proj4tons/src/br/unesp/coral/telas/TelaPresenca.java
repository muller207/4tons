    public void carregarListaPresenca(){
	  ArrayList<Ensaio> l = (ArrayList<Ensaio>) edao.carregarEnsaios();
	  DefaultListModel ensaios = new DefaultListModel();
          if(l!=null && l.size()>0){
	  for (Ensaio e : l) {
			ensaios.addElement(e);
		}	  
		jList1.setModel(ensaios);
	  }else{		
		JOptionPane.showMessageDialog(null, "Nenhum ensaio adicionado!", "Erro", JOptionPane.WARNING_MESSAGE);
	  }
	}

