    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        index = jList1.getSelectedIndex();
            if(index!=-1){
                ArrayList<Ensaio> ensaios = (ArrayList<Ensaio>) edao.carregarEnsaios();
                ensaios.remove(index);
                edao.salvarEnsaio(ensaios);
                DefaultListModel l = (DefaultListModel) jList1.getModel();
                l.removeElementAt(index);
                ensaios = (ArrayList<Ensaio>) edao.carregarEnsaios();
                if(ensaios.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Nenhum ensaio adicionado!", "Erro", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                    JOptionPane.showMessageDialog(null, "Selecione um ensaio!", "Cuidado", JOptionPane.WARNING_MESSAGE);
                }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        index = jList1.getSelectedIndex();    
        Ensaio e = (Ensaio) jList1.getSelectedValue();
            if(index!=-1){
                dispose();
                TelaAddEnsaio t = new TelaAddEnsaio(index, e);
                t.setTitle("Editar Ensaio");
                t.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                t.setVisible(true);
            }else{
                    JOptionPane.showMessageDialog(null, "Selecione um ensaio!", "Cuidado", JOptionPane.WARNING_MESSAGE);
                }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Ensaio e = (Ensaio) jList1.getSelectedValue();
        if(e!=null){
            dispose();
            TelaMostrarEnsaio t = new TelaMostrarEnsaio(e);                        
            t.setResizable(false);
            t.setTitle(e.toString());
            t.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            t.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um ensaio!", "Cuidado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

